// Helper to get relationships for a specific person
export function getRelationshipsForPerson(personId: string): Relationship[] {
  return relationships.filter(
    (r) => r.personId1 === personId || r.personId2 === personId
  );
}

// Helper to get all unique person IDs involved in relationships
export function getAllRelatedPersonIds(): string[] {
  const ids = new Set<string>();
  relationships.forEach((r) => {
    ids.add(r.personId1);
    ids.add(r.personId2);
  });
  return Array.from(ids);
}
