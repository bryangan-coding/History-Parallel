def expand_short_summaries():
    fixed_count = 0
    files_modified = set()
    
    for fp in sorted(glob.glob(f'{PEOPLE_DIR}/*.json')):
        with open(fp, encoding='utf-8') as f:
            data = json.load(f)
        
        modified = False
        for p in data:
            name = p['name']
            if name in EXPANDED:
                old = p.get('summary', '')
                if len(old) < len(EXPANDED[name]):  # Only replace if new is longer
                    p['summary'] = EXPANDED[name]
                    modified = True
                    fixed_count += 1
                    print(f"  Expanded: {name} ({len(old)}→{len(EXPANDED[name])} chars)", file=sys.stderr)
        
        if modified:
            with open(fp, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            files_modified.add(fp)
    
    print(f"\n✅ Expanded {fixed_count} entries across {len(files_modified)} files", file=sys.stderr)

if __name__ == '__main__':
    expand_short_summaries()
