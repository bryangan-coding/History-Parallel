#!/usr/bin/env python3
"""
Tang Dynasty data expansion:
1. Add biographical events for 20+ key figures
2. Enrich short summaries
3. Fix traditional Chinese names → simplified
"""
import json
from pathlib import Path

PEOPLE_DIR = Path('src/data/people')
EVENTS_FILE = PEOPLE_DIR / '_biographicalEvents.json'

# Load existing events
with open(EVENTS_FILE) as f:
    events = json.load(f)

# Load all people
all_people = []
for f in sorted(PEOPLE_DIR.glob('*.json')):
    with open(f) as fp:
        data = json.load(fp)
        all_people.extend([p for p in data if 'name' in p])

# Build ID → person map
person_map = {p['id']: p for p in all_people}

def add_event(id, title, titleEn, year, personIds, summary, description, importance=3, tags=None, tagsEn=None):
    events.append({
        "id": id,
        "title": title,
        "titleEn": titleEn,
        "startYear": year,
        "endYear": year,
        "regionId": "tang-dynasty",
        "placeName": "",
        "personIds": personIds,
        "tags": tags or ['人物', '生平'],
        "tagsEn": tagsEn or ['Biography', 'Life'],
        "summary": summary,
        "description": description,
        "importance": importance,
        "datePrecision": "year",
        "isApproximate": False,
        "relatedEventIds": [],
        "sourceIds": [],
        "dataStatus": "published",
        "confidenceScore": 0.9,
        "externalReferences": [],
    })

